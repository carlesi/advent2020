function solution (right, down){
    let numOfTrees = 0;
    let currentRow=0;
    let currentColumn=0;
    let arrayOfLines = inputstr.split('\n');
    while(currentRow<arrayOfLines.length-1){
        currentRow = currentRow+down;
        currentColumn =currentColumn+right;
        if(currentColumn>arrayOfLines[currentRow].length-1) 
            currentColumn = currentColumn- arrayOfLines[currentRow].length;
        if(currentRow< arrayOfLines.length && arrayOfLines[currentRow][currentColumn] ===  '#'){
            numOfTrees++;
        }
    }
    return numOfTrees;
}
